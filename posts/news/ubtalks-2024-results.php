<section>
    <h3>Step Up!</h3>
    <p>Am reperat două probleme notabile cu care se confruntă societatea din ziua de azi: Mobilitatea fizică redusă - de prea multe ori suntem orbiți de comoditatea transportului privat, cu mașina, și uităm să ne bucurăm de cea mai simplă și gratuită metodă de a ne deplasa - mersul pe jos. A doua problemă, deranjantul telefon, care de prea multe ori ne obosește cu notificări, sunete, imagini, videoclipuri, vibrații. Am uitat ce înseamnă să stai în liniște, să nu ai parte de stimuli externi, și să îți odihnești creierul pentru câteva minute.</p>
    <p>Varianta actualizată a aplicației Step Up! prezintă îmbunătățiri față de prototipul existent. În faza de concept, aplicația intenționează să elimine stimulii deranjanți ai telefonului pentru a promova și permite utilizatorului să umble fără să fie deranjat de telefon. La marcarea finalului unei plimbări, aplicația prezintă statistici, și permite distribuirea cu grupul de prieteni a plimbării efectuate. Nu numai atât, dar partea pe care eu o consider interesantă, este că prin activitățile efectuate, se generază bănuți virtuali. Cu acești bănuți virtuali, utilizatorul poate fie să cumpere îmbunătățiri, fie vizuale, pentru etalarea statutului de „activitate”, fie funcționale, care să ducă la generarea a mai multor bănuți. </p>
    <p>De asemenea, un jucător își poate provoca prietenii la activitate fizică. Astfel, se formează o comunitate de oameni care se motivează reciproc în a face activitate în aer liber, dar care de asemenea, prin această activitate, ajung la beneficiile dovedite ale plimbării în aer liber, și își oferă, prin blocarea notificărilor create de telefon, posibilitatea de a se deconecta, măcar pentru o perioadă scurtă de timp, de stresul din lumea ce ne înconjoară. </p>
    <p>Ca elemente tehnice, în momentul de față aplicația trebuie reconstruită pe platformă nativă, pentru a putea să se bucure de funcționalitățile hardware ale telefonului, și a putea bloca notificări și alte setări ale telefonului. De asemenea, intenționez să efectuez un studiu pe detectarea tipurilor de mișcări, pentru a aduce poate, o metodă mai eficientă de determinare a tipului de activitate efectuată și de numărare a pașilor efectuați.</p>
</section>
