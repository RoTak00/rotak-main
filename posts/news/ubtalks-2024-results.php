<section>
    <h3>Ce e UBtalks?</h3>
    <p>După cum am punctat și în scurta mea postare despre înscrierile la UBtalks, Universitatea din București organizează de 11 ani un concurs de prezentare a proiectelor de licență și de cercetare pentru studenții și cerceetătorii Universității.</p>
    <p>Anul acesta, am participat vorbind despre <i>Step Up!</i>, un prototip de aplicație pe care am dezvoltat-o în timpul studiilor în Portugalia, o platformă care intenționează să promoveze mersul și activitatea în aer liber, prin gamificarea experienței de umblat, ținând în același timp utilizatorul cât mai departe de telefon. Totuși, mai multe despre proiectul meu mai târziu. Relevanța concursului, consider eu, stă în experiența de networking și în descoperirea cercetărilor de calitate prezentate de colegii din Universitate. </p>
</section>
<section>
    <h3>Proiecte notabile</h3>
    <p>În această secțiune, prezint proiectele care m-au marcat în cadrul concursului:</p>
    <section>
        <h4>Dacă n-ai un neurodivergent, să-ți cumperi</h4>
        <p>Diana Buhuș a prezentat, aproape teatral, problemele întâmpinate de persoanele care se află pe spectrul ADHD. Aceasta, citând atât din studii, dar și din experiența proprie, a făcut publicul să înțeleagă mai bine dificultățile din viața unei persoane diagnosticate cu afecțiuni din spectrul neurodivergent.</p>
    </section>
    <section>
        <h4>Siguranța femeilor jurnaliste - o parte neglijată a libertății presei</h4>
        <p>Daniela Ioana Vasilache, prin activitatea sa de jurnalism, a trăit și este familiară cu dificultățile care apar în activitatea jurnalistelor. Discutând despre subiecte actuale din jurnalismul românesc, cum ar fi cazurile Emiliei Șercan, sau a Adinei Florea.</p>
        <p>Temerile justificate pe care le întâmpină femeile jurnaliste, și pe care le-a simțit Daniela pe pielea ei, ne duc la o întrebare presantă - Mai e presa liberă?</p>
    </section>
    <section>
        <h4>Cum influențează traficul din București bolile neurodegenerative?</h4>
        <p>În București, traficul și volumul de mașini existente este absolut teribil. Prin cercetarea ei, Maria Alexandra Nuțu studiază efectele particulelor nocive generate de motoarele Diesel, colectate din catalizatoarele unor mașini.</p>
        <p>Aceste particule, pe care le numește intuitiv „particule Diesel”, ajung în creier și duc la inflamarea țesuturilor acestuia. Cercetarea Mariei este în plină activitate, iar ea caută să obțină mai multă finanțare din partea Universității pentru finalizarea cu succes a studiului ei.</p>
    </section>
    <section>
        <h4>Evoluția imaginii feminine în secolul al XX-lea. Vestimentație și machiaj</h4>
        <p>Prezentând un studiu amplu despre vestimentația feminină din întreg secolul al XX-lea, Măriuca Drăgoi s-a prezentat îmbrăcată într-un stil reprezentativ anilor 1940. Activitatea sa a constat în construirea, prezentarea, și imortalizarea prin fotografii a diferitelor stiluri vestimentare din fiecare decadă a anilor 1900.</p>
        <p>Am fost uimit enorm de dedicația ei și de măestria de care a dat dovadă prin reproducerea prin forțe și resurse proprii a peste 20 de stiluri vestimentare.</p>
    </section>
    <section>
        <h4>Alte proiecte notabile</h4>
        <p>Alte proiecte pe care le-am apreciat au vorbit despre rolul actual al științei, despre biblioteca publică ca factor de incluziune în comunitate, despre greșelile gramaticale în melodiile românești, despre Game Of Thrones și modul nedrept în care acesta s-a încheiat, idei ingenioase de marketing pentru promovarea Vămii Vechi, o prezentare interesantă despre Symphonic Black Metal, despre comunicarea instituțională și modul în care studenții văd Universitatea. De asemenea, proiecte interesante au fost prezentate despre nevoia valorificării cimitirului multietnic din Sulina, diferite observații legate de influența limbii vorbite în gândire, evoluția unui oraș sustenabil, nevoia educației muzicale în școli, relația dintre vitamina D și celula epitelială intestinală, despre fanii anime și comic con, sau despre fenomenul de burnout în profesori.</p>
        <p>Pe această cale, vreau să felicit pe toți cei care au ajuns în finală, pentru dedicația de care dau dovadă și pentru studiile incredibile pe care le efectuează. Cred sincer că lumea e mai bună cu oameni ca voi!</p>
</section>
