    <h3>Etapa 2024 a Games of Science</h3>
    <p>Anul acesta, mă bucur să fi fost numit ambasador al Games of Science. Am reușit să promovez evenimentul în cercul meu, iar asta a adus-o în concurs pe Otilia Pintilii. Aceasta a câștigat etapa din Iași a evenimentului, iar mai apoi a cucerit juriul finalei și a câștigat ediția a 3-a a Games of Science, cu un discurs despre dezvoltarea unei inimi artificiale de titan.</p>
