<section>
    <h3>De ce GoS special?</h3>
    <p>Formatul concursului e ce consider eu că îl diferențiază de altele. E extrem de brutal, dar totuși incredibil de simplu: Cum așa?</p>
    <p>În viață, e necesar să captezi atenția celor cu care încerci să comunici. Nimeni nu are timp, toată lumea este stresată. Games of Science demonstrează că 15 secunde sunt de ajuns. Există 16 participanți în concurs, care se întrec 2 câte 2, în stil eliminatoriu. În etapa 1, participanții au doar 15 secunde să își prezinte subiectul și să convingă juriul că ei sunt cei care merită ascultați mai mult. Cei care pierd, sunt eliminați. În 15 secunde, poți pierde totul.</p>
    <p>Etapa a doua permite concurenților să își prezinte subiectul în 30 de secunde, sferturile de finală în 60 de secunde. Doar în semifinală și în finală participanții au timp să detalieze subiectul lor. În finală, participanții au 3 minute ca să detalieze subiectul lor de cercetare.</p>
    <p>O paranteză pe care o consider importantă, sunt bucuros să știu că am fost eliminat din concurs de persoana care mai apoi a câștigat ediția 2023 a concursului, Florin Teleanu, cercetător în cadrul proiectului ELI-NP, cu discursul său despre legătura dintre evenimentele cuantice și fenomenele biologice.</p>
</section>
