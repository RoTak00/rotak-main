<section>
    <h3>Ce înseamnă pentru mine Games of Science?</h3>
    <p>Games of Science a fost pentru mine, în anul 2023, o primă introducere în Public Speaking și în metodele de a-ți face discursul, în orice ar consta acesta, nu numai auzit, dar și ascultat de către public. Evenimentul începe cu o zi întreagă de training interactiv și social, cu o eficiență incredibilă, care prezintă, pe scurt, tot ce e necesar pentru un discurs de succes. </p>
    <blockquote cite = "https://www.gamesofscience.org/despre-gos">Games of Science este un proiect de promovare a comunicării științei pe înțelesul publicului larg, constituit din sesiuni de training cu tineri oameni de ştiinţă, la capătul cărora participanții se întrec într-un concurs de prezentări științifice.</blockquote>
    <p>Prima mea participare, și unica până în momentul de față, a avut loc în momentul în care mă aflam în anul 1 de studii de licență. La a doua ediție a concursului, am luat parte la training-uri și am vorbit despre un subiect care era foarte în temă în momentul respectiv. Inteligența artificială - planul meu a fost să demaschez miturile existente și să explic modul în care funcționează algoritmii AI. Nu m-am calificat la finală, dar am obținut mult mai multe: discursurile celor cu care am participat la ediția locală a evenimentului m-au învățat despre fizică, psihologie, informatică, și nu numai. De atunci, consider acest concurs o adevărată comoară de cunoștințe.</p>
</section>
